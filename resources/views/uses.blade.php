            <x-heading>Software</x-heading>

            <x-use title="Alfred" link="https://www.alfredapp.com">
                I use Alfred <i>all the time</i>. It is my app launcher & switcher, my clipboard history manager, my
                workflow launcher, and my snippet expander. It is incredibly versitile and well worth the cost of the
                power pack.
            </x-use>

            <x-use title="Code editor">
                My primary IDE is whatever <a href="https://www.jetbrains.com/products/#type=ide" target="_blank" rel="noreferrer" class="link">Jetbrains IDE</a>
                works best for the language I am developing in at the time. On PHP projects, this is PhpStorm. For Go, Goland.
                If I am writing Java, Intellij it is. For simple throwaway scratch files and quick editing (that does not require an IDE),
                I use <a href="https://code.visualstudio.com" target="_blank" rel="noreferrer" class="link">Microsoft VS Code</a>.
            </x-use>

            <x-use title="1Password" link="https://1password.com">
                1Password is by far the best password manager I have used to date. After years of having a cobbled together
                solution of Dropbox + KeePass + lots of frustration, I made the switch and have not looked back since.
            </x-use>

            <x-use title="Things" link="http://culturedcode.com/things/">
                Having used no proper todo app for the longest time, I finally decided to start using one after reading the
                book <a href="https://www.goodreads.com/book/show/1633" target="_blank" rel="noreferrer" class="link">Getting Things Done</a>.
                This has proven to be the best thing I could have done. And since I already up to my waste into the Apple ecosystem,
                I figured I would pick up the best todo app available.
            </x-use>

            <x-use title="Obsidian" link="https://obsidian.md">
                My note-taking app of choice is Obsidian. I've tried them all, but the simplicity of Obsidian paired with
                its incredibly powerful theming and plugin system just cannot be beat. My setup is fairly simple with
                some quality-of-life enhancing plugins installed, with everything syncing seamlessly over iCloud.
            </x-use>

